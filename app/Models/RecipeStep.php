<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class RecipeStep extends Model
{
    protected $table = 'recipe_steps';
   protected $fillable = [
       'recipe_id',
       'step_number',
       'title',
       'image',
       'instruction',
   ];

    public function recipe()
    {
         return $this->belongsTo(Recipe::class);
    }
}
